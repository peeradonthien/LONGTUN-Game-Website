// ฟังก์ชันในการโหลดข่าวตามรอบ
function loadNews() {
  let currentRound = parseInt(localStorage.getItem('game_round')) || 1;
  if (currentRound < 1 || currentRound > 3) currentRound = 1; // รีเซ็ตหากค่าผิดปกติ

  // คำนวณดัชนีข่าวที่ต้องแสดง
  const startIndex = (currentRound - 1) * 4 + 1;
  const endIndex = startIndex + 3;

  // ดึงข่าวหลัก (ข่าวที่ 1 ของรอบ)
  const mainNewsId = startIndex;
  const mainNews = newsData[mainNewsId];
  if (mainNews) {
    document.querySelector('.home-main .home-img').src = mainNews.image;
    document.querySelector('.home-main .heading-2').textContent = mainNews.title;
    // สมมติว่ามีวันที่และประเภทข่าวในข้อมูล หรือสามารถเพิ่มได้
    // document.querySelector('.home-main .sub-heading').textContent = `${category} | ${date}`;
    // อัปเดตปุ่มอ่านเพิ่มเติม
    document.getElementById('readmorebt').onclick = () => openModal(mainNewsId);
  }

  // ดึงข่าวย่อย (ข่าวที่ 2-4 ของรอบ)
  const listsContainer = document.querySelector('.lists');
  listsContainer.innerHTML = ''; // เคลียร์ข่าวย่อยก่อน

  for (let i = startIndex + 1; i <= endIndex; i++) {
    const news = newsData[i];
    if (news) {
      const listDiv = document.createElement('div');
      listDiv.classList.add('list');

      const img = document.createElement('img');
      img.src = news.image;
      img.alt = `ข่าว ${i}`;

      const contentDiv = document.createElement('div');

      const heading = document.createElement('h3');
      heading.classList.add('heading-3');
      heading.style.cursor = 'pointer';
      heading.textContent = news.title;
      heading.onclick = () => openModal(i);

      const subHeading = document.createElement('p');
      subHeading.classList.add('sub-heading');
      // สมมติว่ามีวันที่และประเภทข่าวในข้อมูล หรือสามารถเพิ่มได้
      // subHeading.innerHTML = `<span>${category}</span> | <span>${date}</span>`;

      contentDiv.appendChild(heading);
      contentDiv.appendChild(subHeading);

      listDiv.appendChild(img);
      listDiv.appendChild(contentDiv);

      listsContainer.appendChild(listDiv);
    }
  }
}

